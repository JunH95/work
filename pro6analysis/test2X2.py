# 교차분석(카이제곱, 카이스퀘어) 가설 검정
# 범주형 자료(변수)를 대상으로 교차 빈도에 대한 기술 통계량을 제공
# 교차빈도에 대한 통계적 유의성을 검증해주는 분석기법
# X² = ∑(관측값 - 기대값)² / 기대값 (window + . -> 수학기호)

# 적합도 : 일원 카이제곱 검정
# 독립성, 동질성 검정 : 이원 카이제곱 검정

# 가설을 채택하는 두 가지 방법 연습
import pandas as pd
data = pd.read_csv("pass_cross.csv", encoding='euc-kr')
print(data.head())
print(data.shape)
print(data.shape[0]) # 행의 개수
print(data.shape[1]) # 열의 개수
print()
# 귀무가설(H0) : 벼락치기 공부하는 것과 합격여부는 독립적이다.
# 대립가설(H1) : 벼락치기 공부하는 것과 합격여부는 독립적이지 않다.
print(data[(data['공부함'] == 1) & (data['합격'] == 1)].shape[0]) # 공부함과 합격이 모두 1인 행의 개수
print(data[(data['공부함'] == 1) & (data['합격'] == 0)].shape[0]) # 공부함과 불합격이 모두 1인 행의 개수
print(data[(data['공부함'] == 0) & (data['합격'] == 1)].shape[0]) # 공부 안함과 합격이 모두 1인 행의 개수
print(data[(data['공부함'] == 0) & (data['합격'] == 0)].shape[0]) # 공부 안함과 불합격이 모두 1인 행의 개수

ctab = pd.crosstab(index=data['공부함'], columns=data['합격'], margins=True)
ctab.columns = ['불합격', '합격', '합계']
ctab.index = ['공부 안함', '공부함', '합계']
print(ctab) # 관찰값(빈도)

# 기대도수 = (각 행의 주변합)*(각 열의 주변합) / 전체합
ctab['기대불합격'] = ctab['합계'] * ctab.loc['합계', '불합격'] / ctab.loc['합계', '합계']
ctab['기대합격'] = ctab['합계'] * ctab.loc['합계', '합격'] / ctab.loc['합계', '합계']
print(ctab)
#          불합격  합격  합계  기대불합격  기대합격
# 공부 안함   13   12    25     10.0      15.0
# 공부함      7    18    25     10.0      15.0
# 합계       20    30    50     20.0      30.0

# X² = ∑(관측값 - 기대값)² / 기대값
# X² = (13 - 10)² / 10 + (12 - 15)² / 15 + (7 - 10)² / 10 + (18 - 15)² / 15 = 3.0
# 자유도 = (행의 개수 - 1) * (열의 개수 - 1) = 1
# 유의수준 0.05에서 자유도 1인 카이제곱 분포의 임계값은 3.84
# 임계값보다 X²가 크면 귀무가설 기각, 작으면 귀무가설 채택
# X² = 3.0 < 3.84 이므로 귀무가설 채택, 공부함과 합격은 독립적이다.
# 그러므로 벼락치기 공부하는 것과 합격여부는 독립적이다.라는 의견이 통계적으로 유의미하다.

# 검정방법2 : p-value
# p-value : 귀무가설이 참일 때, 관측된 데이터보다 극단적인 결과가 나올 확률
# p-value가 유의수준보다 작으면 귀무가설 기각, 크면 귀무가설 채택
import scipy.stats as stats

chi2, p, _, _ = stats.chi2_contingency(ctab) # 교차표에서 주변합과 합계는 제외하고 검정
print(chi2, p) # X² 통계량과 p-value
# X² = 3.0에 대한 p-value는 약 0.93
# 유의수준 0.05보다 p-value(0.93)가 크므로 귀무가설 채택, 공부함과 합격은 독립적이다.

# chi2 검정 정식명칭 : Perason's chi-suare test
